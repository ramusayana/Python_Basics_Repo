#IMPLICIT ND EXPLICIT TYPE CONVERSION
# Implicit type conversion occurs when Python automatically converts one data type to another.
# For example, when adding an integer and a float, Python converts the integer to a float.
# Explicit type conversion occurs when you manually convert one data type to another using built-in functions.
# Example of implicit type conversion
num1 = 10  # int
num2 = 3.14  # float
result = num1 + num2  # Implicit conversion of int to float
print("Result of implicit conversion:", result)  # Output: 13.14
# Example of explicit type conversion
num3 = 10  # int
num4 = 3.14  # float
result_explicit = num3 + int(num4)  # Explicit conversion of float to int
print("Result of explicit conversion:", result_explicit)  # Output: 13

